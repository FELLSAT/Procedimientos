CREATE OR REPLACE VIEW PACIENTESAIAS
 -- =============================================      
 -- Author:  FELIPE SATIZABAL
 -- =============================================
AS 
	SELECT MOVI_CARGOS.NU_HIST_PAC_MOVI ,
		PAQUETES.CD_CODI_SER_PAQU 
	FROM MOVI_CARGOS 
	INNER JOIN PAQUETES    
		ON MOVI_CARGOS.NU_NUME_MOVI = PAQUETES.NU_NUME_MOVI_PAQU
	WHERE (PAQUETES.CD_CODI_SER_PAQU IN ( '8711','8712','8713','8714','8715','8751','8752','8753','8781','8782','8783','8784','8785'))
		AND NU_NUME_FAC_MOVI = 0
		AND NU_ESTA_MOVI <> 2;